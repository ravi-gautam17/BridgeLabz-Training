
// this is the program to convert distance from kilometre to miles
public class ConvertFromKMtoMiles {
    public static void main(String[] args) {
        double distanceInKM=10.8;
        //calculate the distance in miles
        double distanceInMiles=distanceInKM*0.621371;
        System.out.println("The distance "+distanceInKM+"km in miles is "+distanceInMiles);

    }
}
